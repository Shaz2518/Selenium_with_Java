
public class EvenNumbers {

	public static void main(String[] args) {
		int count =0;
		for(int i=1; i<=100; i++)
		{
			if (i%3==0)
			{
			//	System.out.println(i);
				count++;
			}
		}
		//System.out.println("Even Numbers between 1-100 are: " + count);
		
		//Table of 19
		for(int i=1; i<=10; i++)
		{
			System.out.println(i*19);
		}
		//System.out.println("Even Numbers between 1-100 are: " + count);
		
		
	}

}
